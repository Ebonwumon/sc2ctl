<strong>Note:</strong> The Admins reserve the right to modify or change these rules in exceptional circumstances.

<p>
	<h3>Rosters</h3>
	<ul>
		<li>
			A team must have a minimum of five members on the roster to be elligible to participate
		</li>
		<li>
			Teams may <strong>not</strong> send out any player who is not on their SC2CTL roster. Members must be on
			the SC2CTL roster for at least six (6) days before they are elligible to play.
		</li>
		<li>
			A player may not play on two SC2CTL teams at the same time 
			in the same season. If one team disbands, that member may
			move to a different team, however.
		</li>
		<li>
			At time of registration, the highest rank member must be less than or equal to the division requirements of
			the league he or she is entering.
		</li>
	</ul>
</p>

<p>
	<h3>Timeliness</h3>
	<ul>
		<li>Teams must have their matches played by the posted due date in the bracket. If this cannot be done teams
			<strong>must</strong> contact an admin, or no exceptions will be granted.
		</li>
		<li>
			If one team "no-shows" for a scheduled match, they will recieve a DQ for that round.
			<br /><em>No shows are defined as:</em>
			<ol>
				<li>Being 15 minutes late to the scheduled match without notifying the other team in any manner</li>
				<li>Not making an effort to schedule the match (eg. not filling out the doodle at all)</li>
			</ol>
		</li>
		SC2CTL uses a tool called Doodle for scheduling matches. We recommend that teams use that as it is not only 
		convenient, but it shows, without a shadow of a doubt who made the effort to schedule the match and which 
		teams were truant in responding. If you do not wish to use Doodle, that is your choice, however if you do not
		and you wish to request a DQ for a non-responsive team, <strong>the burden of proof to show that you made
		scheduling attempts and they did not will be on you</strong>.
		<li>
			Match DQ's will be assigned as follows:
			<ol>
				<li>If one team no-shows, that team will recieve a DQ</li>
				<li>If both teams no-show, the match will either be ignored, or results randomly generated.</li>
				<li>If teams are consistently truant in their matches, they may be removed from the league altogether</li>
			</ol>
		</li>
	</ul>
</p>

<p>
	<h3>Playing Matches</h3>
	<ul>
		<li>All matches are best-of nine (a team must win five games to win the match)</li>
		<li>The first game of a match will start on the map in the map pool whose number corresponds with the week
			number in the current SC2CTL Season. For example, in Week 3 of SC2CTL, matches will start on map #3. 
			If seasons go longer than nine weeks, the map pool then repeats (on week 10, players will play on map #1.
		</li>
		<li>Map picks are losers-choose after the first game. No map may be repeated in a best-of nine.</li>
		<li>Players must choose one race for a best-of nine. Random is an acceptable race, however if the player
			wishes to play random he must select "Random" from the race selection menu. He may not choose the race
			before each game.
		</li>
		<li>When choosing the starting players for the best-of nine, teams must announce their choices simultaneously.
			I suggest having a third player count down, and both captains press enter on their typed selection at the
			same time.
		</li>
		<li>During group stages, teams may "revive" a defeated player to play again. In order to revive, the team
			must have already had 4 players defeated (eg. you can't revive a player after only losing three games).
			Revival is <strong>not</strong> allowed during the playoff stage.
		</li>
		<li>
			If a teams runs out of players (eg. only 3 members are online and the team loses three games) the remaining
			matches will be forfeits for them.
		</li>
	</ul>
</p>

